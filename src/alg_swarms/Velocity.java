package alg_swarms;

/**
 *
 * @author Andreas
 */
public class Velocity {

    private double x;
    private double y;

    public Velocity(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Velocity{" + "x=" + x + ", y=" + y + '}';
    }
    
}
